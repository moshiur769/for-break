const grade = 50;

switch (true){
    case grade >=90:
        console.log('A+');
        break;

    case grade >=80:
        console.log("A");
        break;
        
    case grade >=70:
        console.log("B+");
        break;
        
    case grade >=60:
        console.log("B");
        break;
        
    case grade >=50:
        console.log("c+");
        break;
        
    case grade >=40:
        console.log("c");
        break;
        
    case grade >=30:
        console.log("F");
        break;    
}